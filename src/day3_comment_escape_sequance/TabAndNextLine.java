package day3_comment_escape_sequance;

public class TabAndNextLine {
    public static void main(String[] args) {
        // this is without any tab
        System.out.println("This is not tabbed");
        // this is with one tab
        System.out.println(" This is tabbed once");
        System.out.println("This is tapped twice");


        System.out.println("This is tapped twice");

        System.out.println("1) Go to store");
        System.out.println("2) Buy some banana");
        System.out.println("3 Go to the work");
        System.out.println("\n1)Go to store\2n)buy some banana\3n)go to the work");



    }
}
